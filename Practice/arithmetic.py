print(10 / 3)  # Float Quotient
print(10 // 3)  # Integer Quotient
print(10 % 3)  # Remainder
print(10 ** 3)  # Exponent

print(2 * 3 + 10)  # Operator precedence
print(10 + 2 * 3)

x = 2.9
print(round(x))  # Round off

y = -2.9
print(y)
print(abs(y))  # Absolute value

import math

print(math.floor(x))
print(math.ceil(x))
